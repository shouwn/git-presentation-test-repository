from fraction import Fraction

f1 = Fraction(2, 3)
f2 = Fraction(3, 4)

print(f1, "+", f2, "=", f1 + f2)
print(f1, "-", f2, "=", f1 - f2)
print(f1, "*", f2, "=", f1 * f2)
print(f1, "/", f2, "=", f1 / f2)
print(f1, "==", f2, "=", f1 == f2)
print(f1, "!=", f2, "=", f1 != f2)
print(f1, "<", f2, "=", f1 < f2)
print(f1, "<=", f2, "=", f1 <= f2)
print(f1, ">", f2, "=", f1 > f2)
print(f1, ">=", f2, "=", f1 >= f2)